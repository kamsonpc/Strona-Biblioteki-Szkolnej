Księgozbiór biblioteki liczy ponad 15 000 woluminów. Gromadzimy 
literaturę piękną i książki ze wszystkich dziedzin wiedzy. Wydzielony jest 
księgozbiór podręczny, zawierający encyklopedie ogólne i specjalne, 
różnorodne słowniki do języka polskiego i języków obcych, atlasy, albumy oraz 
wybrane pozycje z zakresu nauki o literaturze, historii powszechnej, techniki, 
mechaniki, elektroniki i innych dziedzin nauki. 
<br>
<br>
Księgozbiór podzielony jest na działy według Uniwersalnej Klasyfikacji 
Dziesiętnej, natomiast książki w poszczególnych działach ułożone są 
alfabetycznie. Dodatkowo wydzielony jest dział lektur szkolnych, co ułatwia 
naszym czytelnikom wyszukiwanie. 
<br>
<br>
Biblioteka gromadzi również nowości wydawnicze z dziedziny 
beletrystyki, które są stale uzupełniane.
<br>
<br>
