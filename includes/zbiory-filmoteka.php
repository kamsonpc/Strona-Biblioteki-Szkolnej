Biblioteka posiada również zbiór adaptacji filmowych i teatralnych lektur szkolnych oraz filmów wymienionych w podstawie programowej z języka polskiego. 
<br>
Adaptacje teatralne: 
Ferdydurke -reż. Maciej Wojtyszko, Pieśń o Tristanie i Izoldzie – reż. Krystyna Janda, Kordian – reż. Jan Englert, Dziady – reż. Jan Englert, Tartuffe, czyli Obłudnik – reż. Andrzej Seweryn, Rozmowy z Katem – reż. Maciej Englert, Moralność pani Dulskiej – reż. Tomasz Zygadło, Tango - reż. Maciej Englert, Pamiętnik z Powstania Warszawskiego – reż. Maria Zmarz-Koczanowicz, Poskromienie złośnicy – reż. Jonathan Miller, Makbet – reż. Jack Gold, Świętoszek – reż. Zygmunt Hübner. 
<br>
<br>
Adaptacje filmowe: 
Dziady (Lawa), Ludzie bezdomni, Chłopi, Przedwiośnie, Dżuma, Proces, Granica, Lalka, Pożegnanie z Marią, Nad Niemnem, Noce i Dnie, Wesele, Ferdydurke, Ogniem i Mieczem, Potop, Kamizelka, Tragedia Makbeta, Quo vadis, Pan Tadeusz, Zbrodnia i kara. 
<br>
<br>
Filmy z podstawy programowej z języka polskiego: 
Kanał – reż. Andrzej Wajda, Czas Apokalipsy – reż. Francis Ford Coppola, Franciszek – reż. Liliana Cavani , Katyń – reż. Andrzej Wajda, Niech zstąpi Duch Twój... - Homilia wygłoszona przez Jana Pawła II podczas mszy na Placu Zwycięstwa w 1979 roku, Dzieci Ireny Sendlerowej – reż. John Kent Harrison. 
<br>
<br>
W naszych zbiorach znajdują się również filmy dokumentalne o tematyce historycznej, przyrodniczej oraz filmy fabularne. Osobną grupę stanowią filmy o zagrożeniach współczesnego świata (m.in. o agresji, narkomanii, paleniu tytoniu, alkoholizmie, używkach, przemocy, cyberprzemocy itp.).